package Day2_PrintandVariables;

public class Topic5_Variables {

    public static void main(String[] args) {
        // variables are the containers where they hold the values for us
        /*Primitive Data Types
        1) boolean, holds 2 values, true of false
        2) byte, holds the whole number in range of -128 to 127
        3) char, holds the characters for instance c, or R or " }
        4) short, holds whole nu,ber in the range of -32768 to 32767
        5) int, holds whole number
        6} float, holds decimal numbers up to 7 decimal digit
        7) double, holds decimal numbers up to 16 decimal digit
         */

        // type name = value;

        boolean theName=true;
        System.out.println(theName);

        boolean defValue=false;
        System.out.println(defValue);

        // type name=value

        byte aNumber = 10;
        System.out.println(aNumber);

        byte Ziya = 120;
        System.out.println(Ziya);

        // type name = value (for char values we use '' single quotes)

        char aChar = 'h';
        System.out.println(aChar);
    }
}
