package Day2_PrintandVariables;

public class Topic4_EscapeSquence {

    public static void main(String[] args) {

        // \t It is going to leave paragraph space on the console

        System.out.println("This is the line and If I want to put prag space\tMURAT");

        // \n It is going to move on new line on the console

        System.out.println("This is a code where I go to next \nLine");

        // \" prints double quote " on the console

        System.out.println("This is \"Murat\" ");

        // \' prints single quote ' on the console

        System.out.println("This is \'Murat\' 'Murat' ");

        // \\

        System.out.println("\"\\This is Nuredding\\\"");

        //  \t paragraph space
        //  \n new line
        //  \" prints double quote " on the console
        //  \\ prints \ on the console
        //  \' prints single quote
        //  '  prints single quote

        // I want to print \Nureddin\

        System.out.println("\\Nureddin\\\\");
    }
}
